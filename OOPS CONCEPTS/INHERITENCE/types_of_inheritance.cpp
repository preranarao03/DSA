/* TYPES OF INHERITANCE

   The types of inheritance are:

   (i). Single-Level:
         -> ex - as seen in the Human class and Male class example

    (ii). Multi-Level:
          -> ex -  B class derives from A class, C derives from B class, D class derives from C class etc..

    (iii). Multiple-Inheritance:
          -> ex -  class C inherits from both classes A and B

    (iv). Hierarchical-Inheritance:
          -> one class serves as parent class for more than one class
          -> ex - A class acts as parent for both B and C classes

    (v). Hybrid-Inheritance:
          -> A combination of more than one type of inheritance
          -> ex - combination of hierarchical and multiple inheritance


 */