/* TYPECASTING
    -> converting from one datatype to another datatype
*/

#include <iostream>
using namespace std;

int main()
{

    int a = 'a';
    cout << a << endl; // 'a' is typecasted to an integer to give ascii value as output

    char b = 78;
    cout << b << endl; // 78 is typecasted to a character
}